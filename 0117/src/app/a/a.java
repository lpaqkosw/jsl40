package app.a;

import java.util.Scanner;

public class a{
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int cnt=0;
        Parking[] cars = new Parking[100];

        for(int i = 0 ; i < cars.length; i++){
            int input = s.nextInt();
            if(input == 0000){
                break;
            }
            cnt++;
            String carNum = Integer.toString(input);
            int time = s.nextInt();

            cars[i] = new Parking(carNum, time);
        }

        ParkingData data = new ParkingData(cars, cnt);
        data.printData();
    }
}



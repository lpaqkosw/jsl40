
public class e1 {

	public static void main(String[] args) {
		int a=5;
		System.out.println("a="+ a++); // a=6
		System.out.println("a="+ a--); // a=5
		System.out.println("a="+ -a);  // -5
		System.out.println("a="+ ~a);  // -6
		System.out.println("a="+ (float)a);
		System.out.println("a="+ (a>3));
		System.out.println("a="+ !(a>3));


		
		
	}

}
